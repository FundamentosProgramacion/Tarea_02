# Descripcion: Calcular distancias y tiempos en un automovil

velocidad = int(input("A que velocidad viaja el auto? "))
t1 = 7
t2 = 4.5
distancia1 = velocidad*int(t1)
distancia2 = velocidad*float(t2)
tiempo3 = int(437)/velocidad

